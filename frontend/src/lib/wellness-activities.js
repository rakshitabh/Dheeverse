// Get activities by recommendation type
export function getActivitiesByType(type) {
  return wellnessActivities[type] || [];
}

// Get a specific activity by ID
export function getActivityById(id) {
  for (const activities of Object.values(wellnessActivities)) {
    const activity = activities.find((a) => a.id === id);
    if (activity) return activity;
  }
  return null;
}

// Get all activities for a recommendation
export function getActivitiesForRecommendation(recommendation) {
  return getActivitiesByType(recommendation);
}

// Get random activity from a type
export function getRandomActivity(type) {
  const activities = getActivitiesByType(type);
  if (activities.length === 0) return null;
  return activities[Math.floor(Math.random() * activities.length)];
}
